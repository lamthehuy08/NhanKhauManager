package Infomation;

public class Info {
    private String MaHo;
    private String ten;
    private String gioiTinh;
    private boolean tvtt;
    private int tuoi;

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public boolean isTvtt() {
        return tvtt;
    }

    public void setTvtt(boolean tvtt) {
        this.tvtt = tvtt;
    }

    public int getTuoi() {
        return tuoi;
    }

    public void setTuoi(int tuoi) {
        this.tuoi = tuoi;
    }


    public String getMaHo() {
        return MaHo;
    }
    public void setMaHo(String maHo) {
        MaHo = maHo;
    }
    public String getTen() {
        return ten;
    }
    public void setTen(String ten) {
        this.ten = ten;
    }

}
